from flask import render_template, request

def shutdown_flask():
	shutdown_server()
	return render_template('options/close.html')


def shutdown_server():
    func = request.environ.get('werkzeug.server.shutdown')
    if func is None:
        raise RuntimeError('Not running with the Werkzeug Server')
    func()